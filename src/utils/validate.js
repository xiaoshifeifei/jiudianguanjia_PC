/**
 * 验证密码 字母 数字 !@#$%^&* 两种组合
 * @param {String} password
 * @return: {Boolean}
 */
export function validatePassword(password) {
  const reg = /^(?![a-zA-z]+$)(?!\d+$)(?![!@#$%^&*]+$)[a-zA-Z\d!@#$%^&*]+$/
  return reg.test(password)
}
