// 非负整数 (正整数 + 0)
export function isNonnegativeInteger(val) {
  const reg = /^\d+$/
  return reg.test(val)
}

//  非零的正整数 (正整数)
export function isNonzeroPositiveInteger(val) {
  const reg = /^[1-9]\d*$/
  return reg.test(val)
}
